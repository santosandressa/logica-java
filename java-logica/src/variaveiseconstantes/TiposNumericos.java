package variaveiseconstantes;

public class TiposNumericos {

	@SuppressWarnings("unused")
	public static void main(String[] args) {
		
		// De -128 até 127
		Byte numeroByte = 1; 
		
		// De -32768 até 32767
		Short numeroShort= 1; 
		
		// De -2147483648 até 2147483647
		Integer numeroInteger = 1;
		
		Long numeroLong = 1L;
		
		Float numeroFloat = 1.0f;
		
		Double numeroDouble = 1.0;
				
	}
}